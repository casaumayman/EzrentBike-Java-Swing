package entities;

/**
 *
 * @author HuyTuan
 */
public class Product {
    private String name;
    private int price;
    private String img;
    private String category;
    private String producer;
    private int id;

    public Product(String name, int price, String img, String category, String producer, int id) {
        this.name = name;
        this.price = price;
        this.img = img;
        this.category = category;
        this.producer = producer;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getProducer() {
        return producer;
    }

    public void setProducer(String producer) {
        this.producer = producer;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
}
